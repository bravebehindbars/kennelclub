 
let sliderImg = document.getElementById('slider-img')


 let images = ['final-pics/boy.png','final-pics/boy2.png','final-pics/boy3.png', 'final-pics/boy4.png']
 let i = 0;
function slide(){
    sliderImg.src = images[i]

    i++;
    if(i===4){
        i=0;
    }
}
setInterval(slide,2000)


var hide_about = document.getElementById("about")

console.log(hide_about)

var displayToggle = ''


	hide_about.style.display = 'none'


displayToggle = 'off'

function toggleAbout() {

if(displayToggle == 'off') {
	
      hide_about.style.display = 'block'
    

    displayToggle = 'on'  
  }

  else if(displayToggle == 'on') {
  	
      hide_about.style.display = 'none'    
    

    displayToggle = 'off'
  }
}



